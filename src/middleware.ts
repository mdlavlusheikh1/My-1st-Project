import { NextResponse } from 'next/server';
import type { NextRequest } from 'next/server';

export function middleware(request: NextRequest) {
  // Get the pathname of the request (e.g. /, /admin/dashboard)
  const path = request.nextUrl.pathname;

  // For development, let the client-side handle authentication
  // This avoids server/client mismatch issues with Firebase Auth
  
  // Only block access to sensitive admin routes if needed
  // Most authentication will be handled by the AuthContext and ProtectedRoute components
  
  return NextResponse.next();
}

// See "Matching Paths" below to learn more
export const config = {
  matcher: [
    /*
     * Match all request paths except for the ones starting with:
     * - api (API routes)
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     */
    '/((?!api|_next/static|_next/image|favicon.ico).*)',
  ],
};